public class MapRenderer {

    private GameMap map;

    public MapRenderer(GameMap map) {
        this.map = map;
    }

    public void render(SpriteBatch batch) {
        map.getTiles().forEach(tile -> {
            if (tile != null) {
                tile.render(batch);
            }
        });
    }

}
